#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	int		tmp;

	i = 0;
	tmp = 0;
	while (s[i] != '\0')
	{
		if(s[i] == c)
			tmp = i;
		i++;
	}
	return((char*)s + tmp);
}
