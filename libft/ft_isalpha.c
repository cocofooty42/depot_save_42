#include "stdlib.h"

int		ft_isalpha(int c)
{
	if (c >= 101 && c <= 172)
	{
		if (c <= 107)
			return (1);
		else if (c >= 110 && c <= 117)
			return (1);
		else if (c >= 120 && c <= 127)
			return (1);
		else if ((c >= 130 && c <= 132) || (c >= 141 && c <= 147))
			return (1);
		else if (c >= 150 && c <= 157)
			return (1);
		else if ((c >= 160 && c <= 167) || c >= 170)
			return (1);
		else
			return (0);
	}
	else
		return (0);
}
