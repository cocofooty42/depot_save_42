#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t		i;
	char		*dest;
	const char	*source;

	i = 0;
	dest = dst;
	source = src;
	while(i < n)
	{
		if(source[i] == c)
			return (dest + i + 1);
		dest[i] = source[i];
		i++;
	}
	return (NULL);
}
