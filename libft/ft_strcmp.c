#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int a;

	a = 0;
	while (s1[a] != '\0' || s2[a] != '\0')
	{
		if ((s1[a] - s2[a]) < 0)
			return ((s1[a] - s2[a]));
		if ((s1[a] - s2[a]) > 0)
			return ((s1[a] - s2[a]));
		if (s1[a] == s2[a])
			a++;
	}
	return (0);
}
