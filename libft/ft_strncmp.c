#include "libft.h"

int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int		i;
	int		c;
	int		m;

	m = n;
	i = 0;
	c = 0;
	while (c == 0 && i < m)
	{
		c = s1[i] - s2[i];
		if (s1[i] == '\0' || s2[i] == '\0')
			return (c);
		i++;
	}
	return (c);
}
